let currentLang = "en";

function applyLanguage(lang) {
    if (!translations[lang]) return;
    const elements = document.querySelectorAll('[data-key]');
    elements.forEach(el => {
        const key = el.getAttribute('data-key');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder') && key.startsWith('plh-')) {
                el.placeholder = translations[lang][key];
            } 
            else if (el.tagName === 'OPTION' && key.startsWith('opt-')) {
                el.textContent = translations[lang][key];
            }
            else {
                if (el.tagName === 'INPUT' && (el.type === 'submit' || el.type === 'reset')) {
                    el.value = translations[lang][key];
                } else {
                    el.textContent = translations[lang][key];
                }
            }
        }
    });
    
    const chooseOption = document.querySelector('#cities option[value=""]');
    if (chooseOption && translations[lang]["opt-choose"]) {
        chooseOption.textContent = translations[lang]["opt-choose"];
    }
    
    const tripTypeSelect = document.getElementById('tripType');
    if (tripTypeSelect) {
        const optionsMap = {
            leisure: translations[lang]["opt-leisure"] || "Leisure & Culture",
            adventure: translations[lang]["opt-adventure"] || "Adventure & Safari",
            historical: translations[lang]["opt-historical"] || "Historical Deep Dive",
            family: translations[lang]["opt-family"] || "Family Friendly"
        };
        for (let i = 0; i < tripTypeSelect.options.length; i++) {
            const opt = tripTypeSelect.options[i];
            if (optionsMap[opt.value]) {
                opt.textContent = optionsMap[opt.value];
            }
        }
    }
    
    const guideSelect = document.getElementById('preferredGuide');
    if (guideSelect && lang !== 'en') {
        const guideLabels = {
            ar: { english: "الإنجليزية", arabic: "العربية", german: "الألمانية", french: "الفرنسية", russian: "الروسية" },
            ru: { english: "Английский", arabic: "Арабский", german: "Немецкий", french: "Французский", russian: "Русский" },
            de: { english: "Englisch", arabic: "Arabisch", german: "Deutsch", french: "Französisch", russian: "Russisch" },
            fr: { english: "Anglais", arabic: "Arabe", german: "Allemand", french: "Français", russian: "Russe" }
        };
        if (guideLabels[lang]) {
            for (let i = 0; i < guideSelect.options.length; i++) {
                const opt = guideSelect.options[i];
                const val = opt.value;
                if (guideLabels[lang][val]) {
                    opt.textContent = guideLabels[lang][val];
                }
            }
        }
    } else if (lang === 'en' && guideSelect) {
        for (let i = 0; i < guideSelect.options.length; i++) {
            const opt = guideSelect.options[i];
            const val = opt.value;
            const defaultNames = { english: "English", arabic: "العربية", german: "Deutsch", french: "Français", russian: "Русский" };
            if (defaultNames[val]) opt.textContent = defaultNames[val];
        }
    }
    
    const guidedLabel = document.querySelector('label[for="guidedTour"]');
    if (guidedLabel && translations[lang]["chk-guided"]) guidedLabel.textContent = translations[lang]["chk-guided"];
    const insuranceLabel = document.querySelector('label[for="travelInsurance"]');
    if (insuranceLabel && translations[lang]["chk-insurance"]) insuranceLabel.textContent = translations[lang]["chk-insurance"];
    const visaLabel = document.querySelector('label[for="visaAssistance"]');
    if (visaLabel && translations[lang]["chk-visa"]) visaLabel.textContent = translations[lang]["chk-visa"];
}

const langSelect = document.getElementById('language-select');
if (langSelect) {
    langSelect.addEventListener('change', (e) => {
        currentLang = e.target.value;
        applyLanguage(currentLang);
    });
}

document.addEventListener('DOMContentLoaded', () => {
    applyLanguage('en');
    const bookLink = document.querySelector('nav ul li a[href="#"]');
    if (bookLink) bookLink.style.fontWeight = 'bold';
    
    const form = document.getElementById('bookingForm');
    if (form) {
        form.addEventListener('submit', (e) => {
            e.preventDefault();
            const name = document.getElementById('name').value.trim();
            const email = document.getElementById('email').value.trim();
            const arrival = document.getElementById('arrival').value;
            const departure = document.getElementById('departure').value;
            const cities = document.getElementById('cities').value;
            const passport = document.getElementById('passport').value;
            
            if (!name || !email || !arrival || !departure || !cities) {
                alert("Please fill all required fields (Name, Email, Arrival/Departure, Destination).");
                return;
            }
            if (!passport) {
                alert("Please upload your passport file.");
                return;
            }
            if (new Date(arrival) >= new Date(departure)) {
                alert("Departure date must be after arrival date.");
                return;
            }
            
            const gender = document.querySelector('input[name="gender"]:checked');
            if (!gender) {
                alert("Please select your gender.");
                return;
            }
            
            const tripType = document.getElementById('tripType').value;
            const guideLang = document.getElementById('preferredGuide').options[document.getElementById('preferredGuide').selectedIndex]?.text || document.getElementById('preferredGuide').value;
            const travelers = document.getElementById('numTravelers').value;
            const guided = document.getElementById('guidedTour').checked;
            const insurance = document.getElementById('travelInsurance').checked;
            const visa = document.getElementById('visaAssistance').checked;
            
            let message = `✨ Booking request received! ✨\n\n`;
            message += `Name: ${name}\n`;
            message += `Email: ${email}\n`;
            message += `Gender: ${gender.value}\n`;
            message += `Destination: ${cities}\n`;
            message += `Dates: ${arrival} to ${departure}\n`;
            message += `Trip Type: ${tripType}\n`;
            message += `Guide Language: ${guideLang}\n`;
            message += `Travelers: ${travelers}\n`;
            message += `Visit Plan Extras: ${guided ? 'Guided Tour ' : ''}${insurance ? 'Insurance ' : ''}${visa ? 'Visa Assist' : 'None'}\n`;
            message += `Services Needed: `;
            const services = [];
            if (document.getElementById('tickets').checked) services.push('Flights');
            if (document.getElementById('car').checked) services.push('Rental Car');
            if (document.getElementById('hotel').checked) services.push('Hotel');
            message += services.length ? services.join(', ') : 'None';
            message += `\n\nOur team will contact you soon via email or phone!`;
            
            alert(message);
            form.reset();
        });
    }
});